    private void continuarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_continuarMouseClicked
        // TODO add your handling code here:
        
        if(campoUsuario.getText().length() > 20 || campoPassword.getText().length() > 20)
        {
            Sonidos.getInstance().reproducir("error"); 
            JOptionPane.showMessageDialog(null, "Alguno de los datos ingresados tiene más de 20 letras!", "Error", 0);  
        }
        else
        {            
           if(campoUsuario.getText().length() < 1 || campoPassword.getText().length() < 1)
           {
                Sonidos.getInstance().reproducir("error");
                JOptionPane.showMessageDialog(null, "Alguno de los campos está en blanco!", "Error", 0);  
           }
           else
           {              
               AdministradorConsultasSQL ad = new AdministradorConsultasSQL(); // BD Remota
               AdministradorBDLocal adL = new AdministradorBDLocal(); // BD Local
             
             if(ad.usuarioCorrecto(campoUsuario.getText(),campoPassword.getText()))
             {                 
               GestorJuego.getInstance().setUbicacionBD(0); 
               
               GestorJuego.getInstance().setUsuario(campoUsuario.getText());  
               GestorJuego.getInstance().setPassword(campoPassword.getText());
               
               ad.continuarPartida(campoUsuario.getText());
               if(!GestorJuego.getInstance().mayorPuntajeRemoto(adL.buscarUsuario(campoUsuario.getText(),campoPassword.getText())))
               {
                  adL.continuarPartida(campoUsuario.getText());
               }               
               
               Sonidos.getInstance().reproducir("nuevorecurso");
               JOptionPane.showMessageDialog(null, "¡Usuario correcto!", "Bienvenido", JOptionPane.PLAIN_MESSAGE, new ImageIcon (getClass().getResource("imagenes/objetos/check.png")));
                              
               setVisible(false);
               dispose();
               
               try {
                   new TablaPuntaje1().setVisible(true);
               } catch (SQLException ex) {
                   Logger.getLogger(ContinuarPartida.class.getName()).log(Level.SEVERE, null, ex);
               }
             }
             else if(adL.usuarioCorrecto(campoUsuario.getText(),campoPassword.getText()))
             {               
               GestorJuego.getInstance().setUbicacionBD(1);   
                 
               GestorJuego.getInstance().setUsuario(campoUsuario.getText());  
               GestorJuego.getInstance().setPassword(campoPassword.getText());
               
               adL.continuarPartida(campoUsuario.getText());
               
               Sonidos.getInstance().reproducir("nuevorecurso");
               JOptionPane.showMessageDialog(null, "¡Usuario correcto!", "Bienvenido", JOptionPane.PLAIN_MESSAGE, new ImageIcon (getClass().getResource("imagenes/objetos/check.png"))); 
                              
               setVisible(false);
               dispose();
               
               try {
                   new TablaPuntaje1().setVisible(true);
               } catch (SQLException ex) {
                   Logger.getLogger(ContinuarPartida.class.getName()).log(Level.SEVERE, null, ex);
               }               
             }
             else
             {
               Sonidos.getInstance().reproducir("error");  
               JOptionPane.showMessageDialog(null, "Usuario incorrecto ...", "Error", 0);  
             }
           }
        }
    }//GEN-LAST:event_continuarMouseClicked

