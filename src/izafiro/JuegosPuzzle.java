    public void cargarPuzzles()
    {
        if(GestorJuego.getInstance().getRecursos()[5])
        {
          totalPuzzles = 7;  
        }
        else if(GestorJuego.getInstance().getRecursos()[4])
        { 
          totalPuzzles = 6;  
        }
        else if((GestorJuego.getInstance().getRecursos()[0] && GestorJuego.getInstance().getPuertas()[1] && GestorJuego.getInstance().getRecursos()[2])
                 || GestorJuego.getInstance().getPuertas()[3])
        {  
          totalPuzzles = 5;  
        }
        else if((GestorJuego.getInstance().getRecursos()[0] && GestorJuego.getInstance().getRecursos()[2])
                 || GestorJuego.getInstance().getPuertas()[1])
        {  
          totalPuzzles = 4;  
        }
        else if(GestorJuego.getInstance().getRecursos()[0])
        {
          totalPuzzles = 2;  
        }
        else if(GestorJuego.getInstance().getRecursos()[1])
        { 
          totalPuzzles = 1;  
        }
    }

