    private void jugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jugarActionPerformed
        // TODO add your handling code here:
        if(totalPuzzles == 0)
        {
          Sonidos.getInstance().reproducir("error");  
          JOptionPane.showMessageDialog(null, "¡Aún no hay Juegos habilitados!");  
        }
        else
        {
          this.setVisible(false);
          this.dispose();
          this.removeAll();
        
          Sonidos.getInstance().detener("portada");
          Sonidos.getInstance().detener("sonidosbosque");
          PruebaPuzzle.getInstance().abrirActividad(numPuzzle, null, null);
        }
    }//GEN-LAST:event_jugarActionPerformed

