    private void siguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_siguienteMouseClicked
        // TODO add your handling code here:
        if(numPuzzle < totalPuzzles)
        {
           Sonidos.getInstance().reproducir("boton"); 
           numPuzzle++;
           this.imagenPuzzle.setIcon(imagenesPuzzle[numPuzzle-1]);
           this.nombrePuzzle.setText(titulos[numPuzzle-1]);
           this.descripcionPuzzle.setText(descripciones[numPuzzle-1]);
        }
    }//GEN-LAST:event_siguienteMouseClicked

