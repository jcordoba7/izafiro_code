    private void continuarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_continuarMouseClicked
        // TODO add your handling code here:
        if(campoUsuario.getText().length() > 20 || campoPassword.getText().length() > 20)
        {
            Sonidos.getInstance().reproducir("error");
            JOptionPane.showMessageDialog(null, "Alguno de los datos ingresados tiene más de 20 letras!", "Error", 0);           
        }
        else
        {
            if(campoUsuario.getText().length() < 1 || campoPassword.getText().length() < 1 || campoPassword2.getText().length() < 1)
            {
                Sonidos.getInstance().reproducir("error");  
                JOptionPane.showMessageDialog(null, "Alguno de los campos está en blanco!", "Error", 0);
            }
            else
            {
                if(!campoPassword.getText().equals(campoPassword2.getText()))
                {
                    Sonidos.getInstance().reproducir("error");
                    JOptionPane.showMessageDialog(null, "Las contraseñas no pueden ser diferentes", "Error", 0);
                }
                else
                {
                    AdministradorConsultasSQL ad = new AdministradorConsultasSQL();
                    AdministradorBDLocal adL = new AdministradorBDLocal();

                    if(!ad.usuarioRepetido(campoUsuario.getText()) && !adL.usuarioRepetido(campoUsuario.getText()))
                    {
                      GestorJuego.getInstance().setUsuario(campoUsuario.getText());  
                      GestorJuego.getInstance().setPassword(campoPassword.getText());

                      adL.ingresarNuevoUsuario(campoUsuario.getText(),campoPassword.getText()); //Usuario en BD Local.
                      ad.ingresarNuevoUsuario(campoUsuario.getText(),campoPassword.getText()); //Usuario en orion.               

                      GestorJuego.getInstance().getEscenarioInicial()[0] = 0;
                      GestorJuego.getInstance().getEscenarioInicial()[1] = 3;

                      Sonidos.getInstance().reproducir("nuevorecurso");
                      JOptionPane.showMessageDialog(null, "¡Tu partida ha sido creada correctamente!","Partida Creada", JOptionPane.PLAIN_MESSAGE, new ImageIcon (getClass().getResource("imagenes/objetos/check.png"))); 
                      
                      setVisible(false);
                      dispose();

                      new SeleccionarGenero().setVisible(true);
                    }
                    else
                    {
                      Sonidos.getInstance().reproducir("error");  
                      JOptionPane.showMessageDialog(null, "El nombre de Usuario que has ingresado ya existe ...\nIntenta con otro.", "Error", 0);  
                    }
                }
            }
        }
    }//GEN-LAST:event_continuarMouseClicked

